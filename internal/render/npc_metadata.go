func NPCDrawPosition(graphicID int, mirrored bool, sx, sy, w, h float64) (float64, float64) {
	drawX := sx - w/2
	drawY := sy - h + npcFootAnchorY

	if meta, ok := npcMetaOffsets[graphicID]; ok {
		if mirrored {
			drawX -= float64(meta.X)
		} else {
			drawX += float64(meta.X)
		}
		drawY -= float64(meta.Y)
	}

	return drawX, drawY
}
